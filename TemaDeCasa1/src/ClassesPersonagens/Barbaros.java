package ClassesPersonagens;

public class Barbaros {

    String nomeBarbaro;
    int vidaBarbaro;
    int ataqueBarbaro;
    int defesaBarbaro;

    public Barbaros(String nomeBarbaro, int vidaBarbaro, int ataqueBarbaro, int defesaBarbaro) {
        this.nomeBarbaro = nomeBarbaro;
        this.vidaBarbaro = vidaBarbaro;
        this.ataqueBarbaro = ataqueBarbaro;
        this.defesaBarbaro = defesaBarbaro;
    }
}
